//In JavaScript, you can use arrays for sorted lists, and sets for sorted sets. Sorting in JavaScript typically requires custom sorting functions.

let mySet = new Set([1, 2, 3, 4, 5]);

// Add an element
mySet.add(6);

// Remove an element
mySet.delete(3);

// Check existence of an element
let exists = mySet.has(4);

// Define sorting on the set
mySet.sort();
